'''
a.	The product of 15 and 38
b.	The product of the sum of the pairs of numbers 3 and 4, and 5 and 9
c.	Integer part of dividing the numbers 7 and 2
d.	The remainder of the division of 48 and 5
e.	Arithmetic mean of the numbers 8, 7, 4, 2
f.	210
g.	Square root of 49 (do not use a function)
h.	25% of 80

'''

print(f"a.  The product of 15 and 38 is {15*38}")
print(f"b.  The product of the sum of the pairs of numbers 3 and 4, and 5 and 9 is {(3+4)*(5+9)}")
print(f"c.  Integer part of dividing the numbers 7 and 2 is {7//2}")
print(f"d.  The remainder of the division of 48 and 5 is {48%5}")
print(f"e.  Arithmetic mean of the numbers 8, 7, 4, 2 is {((8+7+4+2)/4)}")
print(f"f.  2^10 is {2**10}")
print(f"g.  Square root of 49 is {49**(1/2)}")
print(f"h.  25% of 80 is {80*.25}")